public class tell_me_a_joke {
    /**
     * This method prints a simple joke to the console.
     * It doesn't take any parameters and doesn't return any value.
     */
    public static void tellJoke() {
        // Print the setup of the joke
        System.out.println("Why don't scientists trust atoms?");

        // Print the punchline of the joke
        System.out.println("Because they make up everything!");
    }

    public static void main(String[] args) {
        tellJoke(); // Call the method to print a joke
        telljoke();
    }

    // We can mae it here also
    public static void telljoke() {
        System.out.println("Why don't eggs tell jokes?");

        System.out.println("They'd crack each other up!");
    }
}

public class Q5 {
    public static void main(String[] args) {
        // Output is 54 because of the order of operations (PEMDAS)=(Parentheses,
        // Exponents, Multiplication and Division, and Addition and Subtraction)
        // 7 * 49 = 343
        // 343 / 7 = 49
        // 35 / 7 = 5
        // 49 + 5 = 54
        System.out.println(7 * 49 / 7 + 35 / 7);// Output 54

    }
}
